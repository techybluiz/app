# 1. Criar o projeto
npx create-expo-app@latest NewsApp --template blank-typescript

# 2. Navegar para o projeto
cd NewsApp

# 3. Instalar dependências de navegação
npx expo install @react-navigation/native @react-navigation/bottom-tabs

# 4. Instalar dependências do React Navigation
npx expo install react-native-screens react-native-safe-area-context

# 5. Instalar dependências adicionais
npx expo install react-native-vector-icons @expo/vector-icons

# 6. Iniciar o projeto
npx expo start
